function mkHazards(data) {
  return data.map(([x,y,type,param]) => {
    if (type==='spikes' || type==='coral') return { type, x, y, w:param, h:24 };
    if (type==='quicksand') return { type, x, y, w:param, h:14 };
    if (type==='beam') return { type, x, y, w:60, h:8, startX:x, range:param, dir:1, phase:Math.random()*6 };
    if (type==='meteor') return { type, x, y, w:32, h:32, startX:x, range:param, dir:1, rot:0, vy:80 };
    return { type:'saw', x, y, w:40, h:40, startX:x, range:param, dir:1, rot:0 };
  });
}

