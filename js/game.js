  if (pressed('ArrowLeft'))  wmSel=Math.max(0,wmSel-1);
  if (pressed('ArrowRight')) wmSel=Math.min(LAST_WORLD,wmSel+1);
  if (pressed('ArrowUp'))    wmLvl=Math.max(0,wmLvl-1);
  if (pressed('ArrowDown'))  wmLvl=Math.min(2,wmLvl+1);
